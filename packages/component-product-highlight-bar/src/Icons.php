	/**
	 * Resolve one item's icon to safe markup.
	 *
	 * A pasted SVG wins over the picked icon, but only if it survives
	 * sanitizing — otherwise we fall back to the default rather than render
	 * an empty slot.
	 *
	 * @param array $item Item props (icon, customSvg).
	 * @return string Inline SVG markup.
	 */
	public static function markup( array $item ): string {
		$custom = Svg::sanitize( (string) ( $item['customSvg'] ?? '' ) );

		if ( $custom !== '' ) {
			return $custom;
		}

		return self::defaultIcon( (string) ( $item['icon'] ?? '' ) );
	}

	/**
	 * One of the bundled icons, wrapped in a sized, color-inheriting <svg>.
